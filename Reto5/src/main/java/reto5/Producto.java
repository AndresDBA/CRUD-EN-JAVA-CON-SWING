
package reto5;

class Producto {
    
    public int id;
    public String nombre;
    public int precio;
    public String descripcion;
    public String fecha;

    public Producto(int id, String nombre, int precio, String descripcion, String fecha) {
        this.id = id;
        this.nombre = nombre;
        this.precio = precio;
        this.descripcion = descripcion;
        this.fecha = fecha;
    }
    
}
